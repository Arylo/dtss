import test from "ava";
import { d, h, m, M, ms, s, y } from "../lib";

test("Test ms", (t) => {
    t.is(ms(1000), 1000);
    t.is(ms(1), 1);
    t.is(ms(0), 0);
    t.is(ms(-1), -1);
});

